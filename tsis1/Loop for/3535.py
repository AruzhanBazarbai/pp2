#Задача №3535. Пингвины
n=int(input())

for i in range(n):
    print("   _~_   ",end=' ')

print(end="\n")

for i in range(n):
    print("  (o o)  ",end=' ')

print(end="\n")

for i in range(n):
    print(" /  V  \ ",end=' ')

print(end="\n")

for i in range(n):
    print("/(  _  )\\",end=' ')

print(end="\n")

for i in range(n):
    print("  ^^ ^^  ",end=' ')

print(end="\n")

